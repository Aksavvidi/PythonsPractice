fruits = ["apple","banana", "orange", "apple","banana", "orange","apple","banana"]

# Count the apples
print("Numbers of apple: ", fruits.count("apple"))

# sort
fruits.sort()
print("Sorted list:" , fruits)

#reverse
fruits.reverse()
print("Reverse list:", fruits)

# index
print("1st apple exist in index:" , fruits.index("apple"))

#clear
fruits.clear()
print("Cleared list: ", fruits)
